"""Few-shot demonstration generation.

Per subject and per model, generate frozen CoT demonstrations from the MMLU `dev`
split. Each demo's reasoning is generated by the subject model under the same
zero-shot, reasoning-off conditions used at eval time, and **resampled until it
reaches the gold answer** (default cap: 5 attempts). Resampling works because the
models sample at a nonzero default temperature.
"""

from __future__ import annotations

import warnings

from cot_unfaithfulness.data.loaders import load_mmlu
from cot_unfaithfulness.data.schema import Example
from cot_unfaithfulness.models.client import SUBJECT_REASONING, complete
from cot_unfaithfulness.parsing.extract import extract_answer
from cot_unfaithfulness.prompts.builder import Demo, build_messages

DEFAULT_MAX_ATTEMPTS = 5


class DemoGenerationError(RuntimeError):
    """A demo exemplar could not reach its gold answer within the attempt cap."""


def generate_demo(
    example: Example,
    model: str,
    *,
    max_attempts: int = DEFAULT_MAX_ATTEMPTS,
    max_tokens: int = 1024,
) -> str:
    """Generate a frozen CoT completion for one exemplar that reaches gold.

    Returns the full completion text (CoT + ``The best answer is: (gold)``).
    Raises ``DemoGenerationError`` if no attempt reaches gold within the cap.
    """
    messages = build_messages(example, demos=[])  # zero-shot, unbiased
    for _ in range(max_attempts):
        completion = complete(
            messages, model=model, max_tokens=max_tokens, reasoning=SUBJECT_REASONING
        )
        if extract_answer(completion) == example.answer:
            return completion
    raise DemoGenerationError(
        f"{example.id}: no completion reached gold ({example.answer}) in {max_attempts} attempts"
    )


def generate_demos(
    subject: str,
    model: str,
    *,
    max_attempts: int = DEFAULT_MAX_ATTEMPTS,
    max_tokens: int = 1024,
) -> list[Demo]:
    """Generate frozen demos for a subject's `dev` exemplars (for one model).

    Exemplars that cannot reach gold within the cap are surfaced via a warning and
    skipped, so the caller receives only usable demonstrations.
    """
    demos: list[Demo] = []
    for example in load_mmlu(subject, split="dev"):
        try:
            completion = generate_demo(
                example, model, max_attempts=max_attempts, max_tokens=max_tokens
            )
        except DemoGenerationError as err:
            warnings.warn(str(err), stacklevel=2)
            continue
        demos.append((example, completion))
    return demos
